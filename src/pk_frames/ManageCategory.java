    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (!validateFields()) {
            category = new Category();
            category.setName(txtName.getText());

            if (category.isCategoryExist()) {
                category.insert();
                clear();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Name field is required");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoryMouseClicked
        // TODO add your handling code here:
        int index = 0;

        DefaultTableModel model = (DefaultTableModel) tableCategory.getModel();
        index = tableCategory.getSelectedRow();
        category_pk = Integer.parseInt(model.getValueAt(index, 0).toString());
        txtName.setText(model.getValueAt(index, 1).toString());
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableCategoryMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (!validateFields()) {
            category = new Category();
            category.setCategory_id(category_pk);
            category.setName(txtName.getText());

            if (category.isCategoryExist()) {
                category.Update();
                clear();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Name field is required");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

